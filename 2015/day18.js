function lightUp (input, override) {
  override = Object.assign({'#': [], '.': []}, override)

  const initialState = input.trim().split('\n')
    .map(row => row.split('').map(v => v === '#' ? 1 : 0))

  let state = initialState
  override['#'].forEach(coord => {
    state[coord[1]][coord[0]] = 1
  })
  override['.'].forEach(coord => {
    state[coord[1]][coord[0]] = 0
  })

  const blankRow = Array(100).fill(0)
  for (let steps = 1; steps <= 100; steps++) {
    state = state.map((row, j) => {
      const prevRow = state[j - 1] || blankRow
      const nextRow = state[j + 1] || blankRow
      return row.map((d, i) => {
        const neighbors = [
          prevRow[i - 1] || 0,
          prevRow[i],
          prevRow[i + 1] || 0,
          row[i - 1] || 0,
          row[i + 1] || 0,
          nextRow[i - 1] || 0,
          nextRow[i],
          nextRow[i + 1] || 0
        ]
        const sumNeighbors = neighbors.reduce((sum, v) => sum + v, 0)
        return row[i] === 1
          ? ((sumNeighbors === 2 || sumNeighbors === 3) ? 1 : 0)
          : (sumNeighbors === 3 ? 1 : 0)
      })
    })
    override['#'].forEach(coord => {
      state[coord[1]][coord[0]] = 1
    })
    override['.'].forEach(coord => {
      state[coord[1]][coord[0]] = 0
    })
  }

  return state.reduce((sum, row) => {
    return sum + row.reduce((sum, data) => sum + data, 0)
  }, 0)
}

