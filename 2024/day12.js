function totalFenceCost(input, sides = false) {
  const width = input[0].length;
  const height = input.length;

  const edge = new Uint8Array(width * height);
  const vertex = new Uint8Array((width + 1) * (height + 1));

  const assignment = new Uint32Array(width * height);
  const reassigned = new Map();

  let groupCount = 0;
  let group = new Set();
  let runStart = -1;

  for (let j = 0; j < height; j++) {
    const line = input[j];
    const prevLine = input[j - 1];
    for (let i = 0; i < width; i++) {
      if (i > 0 && line[i] === line[i - 1]) {
        // same run
        edge[j * width + i] |= 1;
        edge[j * width + i - 1] |= 4;
      } else {
        // next run
        vertex[j * (width + 1) + i] |= 8;
        vertex[(j + 1) * (width + 1) + i] |= 2;
        const currIndex = j * width + i
        if (group.size > 0) {
          const min = Math.min(...group);
          for (const g of group) {
            if (g > min) {
              reassigned.set(g, min);
            }
          }
          for (let index = runStart; index < currIndex; index++) {
            assignment[index] = min;
          }
        }
        runStart = currIndex;
        reassigned.set(groupCount, groupCount);
        group = new Set([groupCount]);
        groupCount++;
      }

      if (prevLine && line[i] === prevLine[i]) {
        edge[j * width + i] |= 2;
        edge[(j - 1) * width + i] |= 8;
        group.add(assignment[(j - 1) * width + i]);
      } else {
        vertex[j * (width + 1) + i] |= 4;
        vertex[j * (width + 1) + i + 1] |= 1;
      }
    }
  }

  for (let j = 0; j < height; j++) {
    vertex[j * (width + 1) + width] |= 8;
    vertex[(j + 1) * (width + 1) + width] |= 2;
  }

  for (let i = 0; i < width; i++) {
    vertex[height * (width + 1) + i] |= 4;
    vertex[height * (width + 1) + i + 1] |= 1;
  }

  if (group.size > 0) {
    const min = Math.min(...group);
    for (const g of group) {
      if (g > min) {
        reassigned.set(g, min);
      }
    }
    for (let index = runStart; index < width * height; index++) {
      assignment[index] = min;
    }
  }

  for (let k = 0; k < groupCount; k++) {
    reassigned.set(k, reassigned.get(reassigned.get(k)));
  }
  for (let i = 0; i < assignment.length; i++) {
    assignment[i] = reassigned.get(assignment[i]);
  }

  const area = {};
  const perimeter = {};
  const corner = {};

  assignment.forEach((a, k) => {
    area[a] = area[a] || 0;
    area[a]++;

    perimeter[a] = perimeter[a] || 0;
    perimeter[a] += 4;
    if (edge[k] & 1) perimeter[a]--;
    if (edge[k] & 2) perimeter[a]--;
    if (edge[k] & 4) perimeter[a]--;
    if (edge[k] & 8) perimeter[a]--;

    corner[a] = corner[a] || 0;

    const i = k % width;
    const j = Math.floor(k / width);
    if ((vertex[j * (width + 1) + i] & 12) === 12 || (vertex[j * (width + 1) + i] === 3)) corner[a]++;
    if ((vertex[j * (width + 1) + i + 1] & 9) === 9 || (vertex[j * (width + 1) + i + 1] === 6)) corner[a]++;
    if ((vertex[(j + 1) * (width + 1) + i] & 6) === 6 || (vertex[(j + 1) * (width + 1) + i] === 9)) corner[a]++;
    if ((vertex[(j + 1) * (width + 1) + i + 1] & 3) === 3 || (vertex[(j + 1) * (width + 1) + i + 1] === 12)) corner[a]++;
  });

  let total = 0;
  Object.keys(area).forEach(a => {
    total += area[a] * (sides ? corner[a] : perimeter[a]);
  });
  return total;
}

