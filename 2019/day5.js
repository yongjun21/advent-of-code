function runProgram (input, i) {
  const program = [...input]
  const o = []
  let pointer = 0
  while (true) {
    const instruction = getDigits(program[pointer])
    const opcode = (instruction[1] || 0) * 10 + instruction[0]
    if (opcode === 99) break
    const p1 = program[pointer + 1]
    const m1 = instruction[2]
    const v1 = m1 ? p1 : program[p1]
    if (opcode === 3) {
      program[p1] = i
      pointer += 2
    } else if (opcode === 4) {
      o.push(v1)
      pointer += 2
    } else {
      const p2 = program[pointer + 2]
      const p3 = program[pointer + 3]
      const m2 = instruction[3]
      const v2 = m2 ? p2 : program[p2]
      if (opcode === 1) {
        program[p3] = v1 + v2
      } else if (opcode === 2) {
        program[p3] = v1 * v2
      } else if (opcode === 7) {
        program[p3] = +(v1 < v2)
      } else if (opcode === 8) {
        program[p3] = +(v1 === v2)
      } else if (opcode === 5) {
        pointer = v1 ? v2 : pointer + 3
        continue
      } else if (opcode === 6) {
        pointer = v1 ? pointer + 3 : v2
        continue
      }
      pointer += 4
    }
  }
  return o
}

function getDigits (n) {
  const digits = []
  while (n >= 1) {
    const d = n % 10
    digits.push(d)
    n = (n - d) / 10
  }
  if (digits.length === 0) digits.push(0)
  return digits
}

